/**
 * 
  不使用运算符 + 和 - ​​​​​​​，计算两整数 ​​​​​​​a 、b ​​​​​​​之和。
  输入: a = 1, b = 2
  输出: 3
 */

function getSum(a, b) {
  while (b != 0) {
    let carry = (a & b) << 1;
    a = a ^ b;
    b = carry;
  }
  return a;
}

